"""
Database service for planning/calendar integration.
Tables: users, sessions, session_registrations, user_tokens, graph_sync, ics_feeds, message_log
"""

import psycopg2
import psycopg2.extras
import logging
from typing import Optional, Dict, List
from enum import Enum
from db_config import get_db_config

logger = logging.getLogger(__name__)

DB_CONFIG = get_db_config()


class MessageStatus(Enum):
    RECEIVED = "received"
    PROCESSED = "processed"
    FAILED = "failed"
    DUPLICATE = "duplicate"


def _get_connection():
    try:
        conn = psycopg2.connect(
            host=DB_CONFIG["host"],
            port=int(DB_CONFIG["port"]),
            database=DB_CONFIG["name"],
            user=DB_CONFIG["user"],
            password=DB_CONFIG["password"],
        )
        return conn
    except psycopg2.Error as e:
        logger.error("Database connection failed: %s", e)
        raise


# ============================================================================
# USERS
# master_uuid = official UUID from Identity Service (cross-system)
# user_id     = internal UUID auto-generated by PostgreSQL on insert
# ============================================================================

class UserService:
    @staticmethod
    def save(master_uuid: str, email: str) -> bool:
        """Save a user received from Identity Service (idempotent)."""
        try:
            conn = _get_connection()
            cursor = conn.cursor()
            cursor.execute(
                """
                INSERT INTO users (master_uuid, email)
                VALUES (%s, %s)
                ON CONFLICT (master_uuid) DO UPDATE SET email = EXCLUDED.email
                """,
                (master_uuid, email),
            )
            conn.commit()
            cursor.close()
            conn.close()
            logger.info("User saved | master_uuid=%s | email=%s", master_uuid, email)
            return True
        except psycopg2.Error as e:
            logger.error("UserService.save failed: %s", e)
            return False

    @staticmethod
    def get_by_master_uuid(master_uuid: str) -> Optional[Dict]:
        try:
            conn = _get_connection()
            cursor = conn.cursor(cursor_factory=psycopg2.extras.DictCursor)
            cursor.execute(
                "SELECT master_uuid, user_id::text, email, created_at FROM users WHERE master_uuid = %s",
                (master_uuid,),
            )
            row = cursor.fetchone()
            cursor.close()
            conn.close()
            return dict(row) if row else None
        except psycopg2.Error as e:
            logger.error("UserService.get_by_master_uuid failed: %s", e)
            return None

    @staticmethod
    def get_by_user_id(user_id: str) -> Optional[Dict]:
        try:
            conn = _get_connection()
            cursor = conn.cursor(cursor_factory=psycopg2.extras.DictCursor)
            cursor.execute(
                "SELECT master_uuid, user_id::text, email, created_at FROM users WHERE user_id = %s::uuid",
                (user_id,),
            )
            row = cursor.fetchone()
            cursor.close()
            conn.close()
            return dict(row) if row else None
        except psycopg2.Error as e:
            logger.error("UserService.get_by_user_id failed: %s", e)
            return None

    @staticmethod
    def get_by_email(email: str) -> Optional[Dict]:
        try:
            conn = _get_connection()
            cursor = conn.cursor(cursor_factory=psycopg2.extras.DictCursor)
            cursor.execute(
                "SELECT master_uuid, user_id::text, email, created_at FROM users WHERE email = %s",
                (email,),
            )
            row = cursor.fetchone()
            cursor.close()
            conn.close()
            return dict(row) if row else None
        except psycopg2.Error as e:
            logger.error("UserService.get_by_email failed: %s", e)
            return None


# ============================================================================
# MESSAGE LOG — idempotency & tracking
# ============================================================================

class MessageLog:
    @staticmethod
    def log_message(
        message_id: str,
        message_type: str,
        source: str,
        timestamp: str,
        correlation_id: Optional[str] = None,
        status: str = MessageStatus.RECEIVED.value,
        error_message: Optional[str] = None,
    ) -> bool:
        """Log a message for idempotency. Returns False if duplicate."""
        try:
            conn = _get_connection()
            cursor = conn.cursor()
            cursor.execute(
                """
                INSERT INTO message_log
                    (message_id, message_type, source, timestamp, correlation_id, status, error_message)
                VALUES (%s, %s, %s, %s, %s, %s, %s)
                """,
                (message_id, message_type, source, timestamp, correlation_id, status, error_message),
            )
            conn.commit()
            cursor.close()
            conn.close()
            logger.info("Message logged | type=%s | id=%s", message_type, message_id)
            return True
        except psycopg2.IntegrityError:
            logger.warning("Duplicate message ignored | id=%s", message_id)
            return False
        except psycopg2.Error as e:
            logger.error("MessageLog.log_message failed: %s", e)
            return False

    @staticmethod
    def update_message_status(
        message_id: str, status: str, error_message: Optional[str] = None
    ) -> bool:
        try:
            conn = _get_connection()
            cursor = conn.cursor()
            cursor.execute(
                """
                UPDATE message_log
                SET status = %s, error_message = %s, processed_at = NOW(), attempts = attempts + 1
                WHERE message_id = %s
                """,
                (status, error_message, message_id),
            )
            conn.commit()
            cursor.close()
            conn.close()
            return True
        except psycopg2.Error as e:
            logger.error("MessageLog.update_message_status failed: %s", e)
            return False

    @staticmethod
    def get_message(message_id: str) -> Optional[Dict]:
        try:
            conn = _get_connection()
            cursor = conn.cursor(cursor_factory=psycopg2.extras.DictCursor)
            cursor.execute("SELECT * FROM message_log WHERE message_id = %s", (message_id,))
            row = cursor.fetchone()
            cursor.close()
            conn.close()
            return dict(row) if row else None
        except psycopg2.Error as e:
            logger.error("MessageLog.get_message failed: %s", e)
            return None


